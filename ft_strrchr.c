char	*strrchr(const char *s, int c)
{
	char	*ptr;
	int		i;

	ptr = (void *) 0;
	i = 0;
	while (s[i])
	{
		if (s[i] == c)
			ptr = (char *) s + i;
		i++;
	}
	return (ptr);
}
